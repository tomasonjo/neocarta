"""Inference metrics for evaluating SQL generation quality."""

from typing import Any

import pandas as pd
from google.cloud import bigquery


def score_execution_accuracy(
    generated_sql: str,
    gold_sql: str,
    bq_client: bigquery.Client,
    timeout_seconds: int = 30,
) -> dict[str, Any]:
    """
    Score SQL generation by executing both queries and comparing results.

    This is the gold standard for SQL correctness - if the results match,
    the generated SQL is functionally equivalent to the gold SQL.

    Parameters
    ----------
    generated_sql : str
        SQL generated by the LLM
    gold_sql : str
        Ground truth SQL
    bq_client : bigquery.Client
        BigQuery client for execution
    timeout_seconds : int
        Query timeout in seconds

    Returns:
    -------
    dict
        Execution scoring results:
        - execution_match: bool (results are identical)
        - generated_rows: int (number of rows in generated result)
        - gold_rows: int (number of rows in gold result)
        - generated_error: str | None (error from generated SQL)
        - gold_error: str | None (error from gold SQL)
        - row_match_pct: float (percentage of matching rows, 0.0-1.0)
    """
    job_config = bigquery.QueryJobConfig(
        use_query_cache=False,  # Don't use cache for eval
    )

    # Execute generated SQL
    generated_result = None
    generated_error = None
    try:
        query_job = bq_client.query(
            generated_sql,
            job_config=job_config,
            timeout=timeout_seconds,
        )
        generated_result = query_job.result().to_dataframe()
    except Exception as e:
        generated_error = str(e)

    # Execute gold SQL
    gold_result = None
    gold_error = None
    try:
        query_job = bq_client.query(
            gold_sql,
            job_config=job_config,
            timeout=timeout_seconds,
        )
        gold_result = query_job.result().to_dataframe()
    except Exception as e:
        gold_error = str(e)

    # If either query failed, can't compare results
    if generated_error or gold_error:
        return {
            "execution_match": False,
            "generated_rows": len(generated_result) if generated_result is not None else 0,
            "gold_rows": len(gold_result) if gold_result is not None else 0,
            "generated_error": generated_error,
            "gold_error": gold_error,
            "row_match_pct": 0.0,
        }

    # Compare results
    try:
        # First check: exact match (same schema + same data)
        schema_match = set(generated_result.columns) == set(gold_result.columns)

        if schema_match:
            # Same columns - check if data matches
            generated_sorted = generated_result.sort_values(
                by=list(generated_result.columns)
            ).reset_index(drop=True)
            gold_sorted = gold_result.sort_values(by=list(gold_result.columns)).reset_index(
                drop=True
            )
            execution_match = generated_sorted.equals(gold_sorted)
        # Different columns - check if VALUES match (ignoring column names)
        # This handles cases like: COUNT(*) as count vs COUNT(*) as total
        elif generated_result.shape == gold_result.shape:
            # Same dimensions - compare values position by position
            # Sort by values to ensure consistent ordering
            gen_values = generated_result.values
            gold_values = gold_result.values

            # For small results (aggregates), check if values are equal
            try:
                execution_match = pd.DataFrame(gen_values).equals(pd.DataFrame(gold_values))
            except Exception:
                execution_match = False
        else:
            execution_match = False

        # Calculate row match percentage
        if len(gold_result) == 0:
            row_match_pct = 1.0 if len(generated_result) == 0 else 0.0
        else:
            # Count matching rows by value (ignore column names)
            matching_rows = 0
            for idx in range(min(len(generated_result), len(gold_result))):
                try:
                    gen_row = generated_result.iloc[idx].values
                    gold_row = gold_result.iloc[idx].values
                    if len(gen_row) == len(gold_row) and all(
                        (pd.isna(g) and pd.isna(gd)) or g == gd
                        for g, gd in zip(gen_row, gold_row, strict=False)
                    ):
                        matching_rows += 1
                except Exception:
                    pass
            row_match_pct = matching_rows / len(gold_result)

    except Exception:
        # If comparison fails, mark as no match
        execution_match = False
        row_match_pct = 0.0
        schema_match = False

    return {
        "execution_match": execution_match,
        "generated_rows": len(generated_result),
        "gold_rows": len(gold_result),
        "generated_error": None,
        "gold_error": None,
        "row_match_pct": row_match_pct,
        "schema_match": schema_match,
        "value_match": execution_match,  # True if values match (regardless of column names)
    }


def score_execution_accuracy_dry_run(
    generated_sql: str,
    gold_sql: str,
    bq_client: bigquery.Client,
) -> dict[str, Any]:
    """
    Score SQL generation using dry run (validation only, no execution).

    Cheaper and faster than full execution, but only checks syntax validity.
    Use this for quick validation before running full execution metrics.

    Parameters
    ----------
    generated_sql : str
        SQL generated by the LLM
    gold_sql : str
        Ground truth SQL
    bq_client : bigquery.Client
        BigQuery client

    Returns:
    -------
    dict
        Dry run results:
        - generated_valid: bool (generated SQL is syntactically valid)
        - gold_valid: bool (gold SQL is syntactically valid)
        - generated_error: str | None (validation error)
        - gold_error: str | None (validation error)
        - estimated_bytes_processed: int (for generated SQL)
    """
    job_config = bigquery.QueryJobConfig(dry_run=True)

    # Validate generated SQL
    generated_valid = True
    generated_error = None
    estimated_bytes = None
    try:
        query_job = bq_client.query(generated_sql, job_config=job_config)
        estimated_bytes = query_job.total_bytes_processed
    except Exception as e:
        generated_valid = False
        generated_error = str(e)

    # Validate gold SQL
    gold_valid = True
    gold_error = None
    try:
        query_job = bq_client.query(gold_sql, job_config=job_config)
    except Exception as e:
        gold_valid = False
        gold_error = str(e)

    return {
        "generated_valid": generated_valid,
        "gold_valid": gold_valid,
        "generated_error": generated_error,
        "gold_error": gold_error,
        "estimated_bytes_processed": estimated_bytes,
    }
